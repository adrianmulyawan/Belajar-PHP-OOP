// > Visibility Access Level
// 1. PUBLIC
// - Dapat diakses dari classnya
// - Dapat diakses oleh turunannya
// - Dapat diakses dari luar classnya
// 2. PROTECTED
// - Dapat diakses dari classnya
// - Dapat diakses oleh turunannya
// - Tidak dapat diakses dari luar classnya
// 3. PRIVATE
// - Dapat diakses dari classnya
// - Tidak dapat diakses oleh turunannya
// - Tidak dapat diakses dari luar classnya